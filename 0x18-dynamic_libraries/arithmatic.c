#include "arithmatic.h"

/**
* add - function that sum two number
* @a: Numerator
* @b: Denominator
* Return: the result
*/

int add(int a, int b)
{
	return (a + b);
}

/**
* sub - function that subtraction
* @a: Numerator
* @b: Denominator
* Return: the result
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - function that multiplicate two numbers
* @a: Numerator
* @b: Denominator
* Return: the result
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
* div - function that divide two numbers
* @b: Denominator
* @a: Numerator
* Return: the result
*/

int div(int a, int b)
{
	return (a / b);
}

/**
* mod - function that remainder two numbers
* @a: numerator
* @b: denominator
* Return: the result
*/

int mod(int a, int b)
{
	return (a % b);
}
